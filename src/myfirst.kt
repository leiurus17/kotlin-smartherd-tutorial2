fun main(args: Array<String>) {

    var name: String = "Henry"
    println(name)
    name = "Kevin"
    println(name)

    // Other data types in Kotlin
    // Everything in Kotlin is object

    var age: Int = 10
    var isAlive: Boolean = true
    var marks: Float = 97.4F
    var percentage: Double = 90.78
    var gender: Char = 'M'

    println(age)
    println(isAlive)
    println(marks)
    println(percentage)
    println(gender)
}